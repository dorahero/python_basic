# 運算符號基本操作
print('1 + 1 =', 1 + 1)
print('1 - 2 =', 1 - 2)
print('1 * 2 =', 1 * 2)
print('1 / 2 =', 1 / 2)
print('2 的 3 次方為', 2 ** 3)
print('8 除以 5 的餘數為', 8 % 5)
print('8 除以 5 的商為', 8 // 5)

# 運算符號順序
print('1 + 2 * 3 =', 1 + 2 * 3)
print('2 * 2 + 2 * 3 =', 2 * 2 + 2 * 3)
print('1 + 2 ** 3 =', 1 + 2 ** 3)
print('1 + 2 * 3 ** 2 =', 1 + 2 * 3 ** 2)
print('1 + 2 * 3 ** 2 // 4 =', 1 + 2 * 3 ** 2 // 4)
